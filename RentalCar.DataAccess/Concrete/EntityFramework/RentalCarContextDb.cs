using Microsoft.EntityFrameworkCore;
using Microsoft.Extensions.Options;
using RentalCar.Core.Entities.Concrete;
using RentalCar.Entities.Concrete;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace RentalCar.DataAccess.Concrete.EntityFramework
{
    public class RentalCarContextDb : DbContext
    {
        public DbSet<Car> Cars { get; set; }
        public DbSet<Brand> Brands { get; set; }
        public DbSet<Color> Colors { get; set; }
        public DbSet<User> Users { get; set; }
        public DbSet<Customer> Customers { get; set; }
        public DbSet<Rental> Rentals { get; set; }
        public DbSet<CarImage> CarImages { get; set; }
        public DbSet<UserOperationClaim> UserOperationClaims { get; set; }
        public DbSet<OperationClaim> OperationClaims { get; set; }


        public RentalCarContextDb(DbContextOptions options) : base(options)
        {
        }

        public RentalCarContextDb()
        {

        }

        protected override void OnConfiguring(DbContextOptionsBuilder optionsBuilder)
        {
            if (!optionsBuilder.IsConfigured)
            {
                optionsBuilder.UseSqlServer(@"Server=SONER\ADMINSERVER;Database=RentalCarDb;Trusted_Connection=true;MultipleActiveResultSets=false;TrustServerCertificate=Yes");
            }
        }


    }
}
